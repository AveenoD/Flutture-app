SELECT o.id, o.name, COUNT(l.id) AS matching_leads
FROM leads l
JOIN organizations o ON o.id = l.organization_id
JOIN excel_phones e ON right(regexp_replace(l.phone, '\D', '', 'g'), 10) = e.phone_key
WHERE l.deleted_at IS NULL
GROUP BY o.id, o.name
ORDER BY matching_leads DESC;

SELECT l.id, l.name, l.phone, l.source, l.status, o.name AS org
FROM leads l
JOIN organizations o ON o.id = l.organization_id
JOIN excel_phones e ON right(regexp_replace(l.phone, '\D', '', 'g'), 10) = e.phone_key
WHERE l.deleted_at IS NULL
ORDER BY o.name, l.name
LIMIT 20;
